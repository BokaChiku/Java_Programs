/**
 * Java Variable Example: Overflow
 */
package com.javaVariables;

/**
 * @author Kunal24
 * @since 2019
 * @implNote Overflow
 */
public class OverFlow
{
	public static void main(String[] args)
	{
		// Overflow
		int a = 130;
		byte b = (byte) a;
		System.out.println(a);
		System.out.println(b);
	}
}
