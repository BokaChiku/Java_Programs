/**
 * @author Kunal
 * @since 2019
 * @implNote Multiple If Else
 */
public class Multiple_If_Else
{
	public static void main(String[] args)
	{
		int a = 10, b = 20;
		if (a > b)
		{
			System.out.println("A is greater than B");
		}
		else
		{
			System.out.println("B is greater than A");
		}

		if (b < a)
		{
			System.out.println("B is greater than A");
		}
		else
		{
			System.out.println("A is greater than B");
		}
	}
}
