/**
 * Java Infinitive do-while Loop
 */
package com.javaDoWhileLoop;

/**
 * @author Kunal24
 * @since 2019
 * @implNote The Java do-while loop is used to iterate a part of the program
 *           several times.
 * @implNote If you pass true in the do-while loop, it will be infinitive
 *           do-while loop.
 * @implNote Syntax:
 * @implNote do
 * @implNote {
 * @implNote // Code to be executed
 * @implNote }while(true);
 */
public class Infinitive_D_While_Loop
{
	public static void main(String[] args)
	{
		do
		{
			System.out.println("Infinitive do while loop");
		}
		while (true);
	}
}
